package logica;

/**
 *
 * @author Hernaldo Valerio P.
 */
public class Nodo {
    int x;
    int y;
    Nodo sig;
    Nodo ant;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Nodo getSig() {
        return sig;
    }

    public void setSig(Nodo sig) {
        this.sig = sig;
    }

    public Nodo getAnt() {
        return ant;
    }

    public void setAnt(Nodo ant) {
        this.ant = ant;
    }

    public Nodo(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
}
